    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        
        
        
        
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>MuestraTabla</title>");            
            out.println("</head>");
            out.println("<body>");
            out.println("<h1>Fisi #</h1>");
            out.println("<table>");
            
            Connection conn = Conexion.getConexion();
            Statement stm;
            try {
                
                stm = conn.createStatement();
                ResultSet rs = stm.executeQuery("select * from Tabla1 order by id;");
                while(rs.next()){
                    int id = rs.getInt("id");
                    String nombre = rs.getString("nombre");
                    
                    out.println("<tr><td>" + id+"</td>");
                    out.println("<td>" + nombre+"</td></tr>");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(MuestraTabla.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            out.println("</table>");
            out.println("</body>");
            out.println("</html>");
        }
    }

